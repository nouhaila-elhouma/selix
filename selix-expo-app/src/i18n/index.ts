export function translate(language: AppLanguage, key: string, params?: Record<string, string | number>) {
  const dictionary = translations[language] ?? translations.fr;
  const fallback = translations.fr[key];
  const value = dictionary[key] ?? fallback ?? key;
  if (typeof value !== 'string') return key;
  return value.replace(/\{(\w+)\}/g, (_, token) => String(params?.[token] ?? ''));
}
